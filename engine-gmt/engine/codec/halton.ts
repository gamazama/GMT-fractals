/**
 * Halton low-discrepancy sequence — used for TAA jitter and Monte Carlo accumulation.
 */
export const halton = (index: number, base: number) => {
    let result = 0;
    let f = 1 / base;
    let i = index;
    while (i > 0) {
        result = result + f * (i % base);
        i = Math.floor(i / base);
        f = f / base;
    }
    return result;
};
